/**
 * This class explains the use of variables and rules for it.
 * @author Manoj
 *
 */
public class Variables {
    public static void main(String[] args) {
        //variables : These are basically containers or memory locations assigned to store some data.

        String name = "Ramesh"; // Here name is a variable of type String which stores value = "Ramesh".
        int num = 234; // Here num is the variable of type int which stores the value 234 in it.

        // Variable type can be either Primitive Type or Reference Type
        int value = 20; // value is declared and initialized too
        char ch; // here ch is declared but not initialized.
        String str = "JAVA";
        System.out.println(value);
       // System.out.println(ch); // gives error as the variable is declared but not initialised
        System.out.println(str);


    }
}
